public class Area {
    void circle(int r)
    {
        System.out.println("Area of the circle is:"+r*r*3.14);
    }
    void square(int a)
    {
        System.out.println("Area of the Square is:"+a*a);
    }
    void rectangle(int a,int b)
    {
        System.out.println("Area of the rectangle is:"+a*b);
    }
    void triangle(int b,int h)
    {
        System.out.println("Area of the triangle is:"+0.5*b*h);
    }
}
